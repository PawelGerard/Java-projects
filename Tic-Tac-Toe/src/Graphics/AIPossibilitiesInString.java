
package Graphics;

public class AIPossibilitiesInString {
    private int X;
    private int Y;
    private int weight;

    public AIPossibilitiesInString(int x, int y, int weight) {
        this.X = x;
        this.Y = y;
        this.weight = weight;
    }

	public int getX() {
		return X;
	}

	public void setX(int x) {
		X = x;
	}

	public int getY() {
		return Y;
	}

	public void setY(int y) {
		Y = y;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}
    
    
}

